void app_main_activity_start(void* params) {
    ESP_LOGI(LOG_TAG, "Starting %s", __FUNCTION__);
    for (;;) {
        ESP_LOGI(LOG_TAG, "Running...");
        vTaskDelay(1000 / portTICK_PERIOD_MS);
    }
}
